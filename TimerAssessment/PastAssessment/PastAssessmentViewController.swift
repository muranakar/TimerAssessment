    // MARK: - Formatter„ÄÄDouble„ÉªDateÂûã‚ÜíStringÂûã„Å∏Â§âÊõ¥
    private func resultTimerStringFormatter(resultTimer: Double) -> String {
        // Êï¥Êï∞ÈÉ®ÂàÜ
        let integer = Int(resultTimer)
        // Â∞èÊï∞ÈÉ®ÂàÜ
        let fraction = resultTimer.truncatingRemainder(dividingBy: 1)

        var string = ""
        let hour = integer / 3600
        let min = integer / 60
        let sec = integer % 60
        let fracitonConversion = Int(fraction * 100)
        // ÔºëÊôÇÈñì‰ª•‰∏äÁµåÈÅé„Åó„Å¶„ÅÑ„ÅüÂ†¥Âêà
        if hour > 0 {
            string = String(format: "%02dÊôÇ%02dÂàÜ%02dÁßí%02d", hour, min, sec, fracitonConversion)
