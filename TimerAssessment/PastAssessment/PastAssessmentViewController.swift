        guard let assessmentItem = assessmentItem else { return [] }

        switch sortType {
        case .dateAscending:
            return timerAssessmentRepository.loadTimerAssessment(
                assessmentItem: assessmentItem,
                sortBy: "createdAt",
                ascending: true
            )
        case .dateDescending:
            return timerAssessmentRepository.loadTimerAssessment(
                assessmentItem: assessmentItem,
                sortBy: "createdAt",
                ascending: false
            )
        case .timeAscending:
            return timerAssessmentRepository.loadTimerAssessment(
                assessmentItem: assessmentItem,
                sortBy: "resultTimer",
                ascending: true
            )
        case .timeDescending:
            return timerAssessmentRepository.loadTimerAssessment(
                assessmentItem: assessmentItem,
                sortBy: "resultTimer",
                ascending: false
            )
        }
    }

